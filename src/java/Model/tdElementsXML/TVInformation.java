package Model.tdElementsXML;

/**
 *
 * @author Principal
 */
public class TVInformation {

    private int TVManuf, TVModel, TVSerialNum;

    /**
     * @return the TVManuf
     */
    public int getTVManuf() {
        return TVManuf;
    }

    /**
     * @param TVManuf the TVManuf to set
     */
    public void setTVManuf(int TVManuf) {
        this.TVManuf = TVManuf;
    }

    /**
     * @return the TVModel
     */
    public int getTVModel() {
        return TVModel;
    }

    /**
     * @param TVModel the TVModel to set
     */
    public void setTVModel(int TVModel) {
        this.TVModel = TVModel;
    }

    /**
     * @return the TVSerialNum
     */
    public int getTVSerialNum() {
        return TVSerialNum;
    }

    /**
     * @param TVSerialNum the TVSerialNum to set
     */
    public void setTVSerialNum(int TVSerialNum) {
        this.TVSerialNum = TVSerialNum;
    }
}
