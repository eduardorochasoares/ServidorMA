public class IPTVTVInformation {
    private String IPTVTVManuf;
    private String IPTVTVModel;
    private String IPTVTVSerialNum;

    /**
     * @return the IPTVTVManuf
     */
    public String getIPTVTVManuf() {
        return IPTVTVManuf;
    }

    /**
     * @param IPTVTVManuf the IPTVTVManuf to set
     */
    public void setIPTVTVManuf(String IPTVTVManuf) {
        this.IPTVTVManuf = IPTVTVManuf;
    }

    /**
     * @return the IPTVTVModel
     */
    public String getIPTVTVModel() {
        return IPTVTVModel;
    }

    /**
     * @param IPTVTVModel the IPTVTVModel to set
     */
    public void setIPTVTVModel(String IPTVTVModel) {
        this.IPTVTVModel = IPTVTVModel;
    }

    /**
     * @return the IPTVTVSerialNum
     */
    public String getIPTVTVSerialNum() {
        return IPTVTVSerialNum;
    }

    /**
     * @param IPTVTVSerialNum the IPTVTVSerialNum to set
     */
    public void setIPTVTVSerialNum(String IPTVTVSerialNum) {
        this.IPTVTVSerialNum = IPTVTVSerialNum;
    }
    
}
