package Model.tdElementsXML;

/**
 *
 * @author Principal
 */
public class PCInformation {
    private String PCManuf, PCModel, PCSerialNum;

    /**
     * @return the PCManuf
     */
    public String getPCManuf() {
        return PCManuf;
    }

    /**
     * @param PCManuf the PCManuf to set
     */
    public void setPCManuf(String PCManuf) {
        this.PCManuf = PCManuf;
    }

    /**
     * @return the PCModel
     */
    public String getPCModel() {
        return PCModel;
    }

    /**
     * @param PCModel the PCModel to set
     */
    public void setPCModel(String PCModel) {
        this.PCModel = PCModel;
    }

    /**
     * @return the PCSerialNum
     */
    public String getPCSerialNum() {
        return PCSerialNum;
    }

    /**
     * @param PCSerialNum the PCSerialNum to set
     */
    public void setPCSerialNum(String PCSerialNum) {
        this.PCSerialNum = PCSerialNum;
    }
}
